package com.example.webmoduleproject.model.binding;

public class EmploymentDetailsBindingModel {
    private String newJob;
    private String newEmployer;

    public EmploymentDetailsBindingModel() {
    }

    public String getNewJob() {
        return newJob;
    }

    public void setNewJob(String newJob) {
        this.newJob = newJob;
    }

    public String getNewEmployer() {
        return newEmployer;
    }

    public void setNewEmployer(String newEmployer) {
        this.newEmployer = newEmployer;
    }
}
